### Gwarancja — uczciwie, co obejmuje, a czego nie

Tu chcę być szczególnie precyzyjny, bo wokół „gwarancji na implanty" narosło wiele nieporozumień.

**Czego gwarancja zwykle dotyczy:** sytuacji, w których implant zawodzi we wczesnym okresie z przyczyn po stronie leczenia (np. brak osseointegracji). Wówczas mówimy o postępowaniu naprawczym w ustalonym zakresie.

**Czego gwarancja NIE obejmuje — i to klucz:** sam fakt, że „dostaniesz nowy implant w ramach gwarancji", **nie oznacza, że da się go wszczepić bez dodatkowych zabiegów i kosztów**. Jeśli w międzyczasie **zmienią się warunki anatomiczne** — dojdzie do zaniku lub ubytku kości — ponowne wszczepienie może wymagać **dobudowy kości (augmentacji) lub innej dodatkowej chirurgii**. Te zabiegi to osobne procedury i **gwarancja na sam implant ich nie pokrywa**. Innymi słowy: gwarancja na implant ≠ gwarancja na cały proces leczenia w każdych przyszłych warunkach.

**Gwarancja jest też warunkowa** — zależy od przestrzegania zaleceń: higieny, regularnych kontroli, niepalenia. Zaniedbanie tych elementów może oznaczać jej utratę. To uczciwe: klinika odpowiada za jakość leczenia, pacjent za codzienną pielęgnację.

Dlatego **warunki gwarancji omawiamy indywidualnie**, w odniesieniu do Twojego przypadku, zamiast obiecywać „dożywotnią gwarancję na wszystko". Ogólne zasady gwarancji i reklamacji opisujemy na stronie [gwarancje](/gwarancje). Najważniejsze: realna trwałość bierze się z dobrego planowania, jakości wykonania i Twojej współpracy — a nie z traktowania gwarancji jak polisy pokrywającej każdą przyszłą sytuację.

