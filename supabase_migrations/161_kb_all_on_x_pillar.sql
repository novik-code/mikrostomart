### Trwałość — co mówią badania

All-on-4 to nie eksperyment — to **udokumentowany protokół z najdłuższymi obserwacjami w implantologii**. W 18-letnim badaniu obserwacyjnym Maló i wsp. (2019) przeżywalność implantów wyniosła **94,8%**, a prac protetycznych **99,2%**. To jedne z najlepszych długoterminowych wyników w stomatologii odtwórczej.

Trzeba jednak uczciwie powiedzieć: **trwałość zależy nie tylko od zabiegu, ale i od pacjenta**. Implanty służą długie lata, jeśli dbasz o higienę, przychodzisz na kontrole, nie palisz i — przy skłonności do zgrzytania — nosisz szynę ochronną. Więcej o tym (i o tym, czego realnie dotyczy gwarancja) w artykule [All-on-4 — koszt, trwałość i gwarancja](/baza-wiedzy/all-on-4-koszt-trwalosc-gwarancja).

