### Czym jest obciążenie natychmiastowe?

Klasycznie po wszczepieniu implantu czeka się **3-6 miesięcy**, aż zrośnie się on z kością (osseointegracja), i dopiero wtedy zakłada koronę czy most. Obciążenie natychmiastowe „skraca" oczekiwanie pacjenta: implanty od razu spina się **stałym mostem tymczasowym**, który stabilizuje je nawzajem i pozwala normalnie funkcjonować, podczas gdy zrastanie zachodzi „w tle".

Kluczowe rozróżnienie:

* **Most tymczasowy** (24-48 h) — lekki, stały, przykręcony do implantów; nosisz go przez okres gojenia.
* **Most ostateczny** (po 3-6 mies.) — docelowa praca z **cyrkonu przykręcanego**, zakładana po pełnej integracji implantów.

To nie jest „proteza na implantach". To **stały most**, którego nie zdejmujesz samodzielnie.

### Od czego zależy, czy zęby założymy od razu

Natychmiastowe obciążenie jest bezpieczne tylko wtedy, gdy implanty mają wystarczającą **stabilność pierwotną** — czyli mocno trzymają się w kości już w chwili wszczepienia. Tego **nie ocenia się „na oko"**. Mierzymy to obiektywnie:

* **Moment dokręcenia (torque)** — implant powinien osiągnąć **≥35 Ncm**.
* **Wskaźnik ISQ (Osstell)** — pomiar stabilności metodą analizy częstotliwości rezonansowej; wysokie wartości potwierdzają, że implant nadaje się do natychmiastowego obciążenia.
* **Jakość i ilość kości** — oceniona wcześniej na CBCT.

Dopiero gdy te parametry są spełnione, decydujemy się na stały most w 24-48 h. To podejście oparte na pomiarze, a nie na obietnicy — bezpieczeństwo implantów jest ważniejsze niż marketingowe „zawsze w jeden dzień".

### Dla kogo natychmiastowe obciążenie NIE jest najlepsze

Uczciwie: nie u każdego warto obciążać implanty od razu. Ostrożniej podchodzimy lub odraczamy obciążenie, gdy występują:

* **Bardzo miękka, niskiej jakości kość** — implant nie osiąga wymaganej stabilności pierwotnej
* **Bruksizm (zgrzytanie/zaciskanie) bez kontroli** — bez szyny ochronnej nadmierne siły zagrażają gojeniu
* **Intensywne palenie** — pogarsza gojenie i osseointegrację
* **Niekontrolowana cukrzyca** lub inne schorzenia ogólne wpływające na gojenie
* **Niewystarczająca liczba/rozkład implantów** o dobrej stabilności

W takich sytuacjach bezpieczniejszym wyborem jest **klasyczny protokół** (most ostateczny po okresie gojenia). To nie „gorsze leczenie" — to leczenie dopasowane do warunków, które chroni długoterminowy efekt. Jeśli tak jest w Twoim przypadku, powiemy to wprost.

### Jak wygląda dzień zabiegowy

1. **Znieczulenie** (możliwa sedacja) — komfort i brak bólu w trakcie.
2. **Ekstrakcje** zębów niekwalifikujących się do leczenia (jeśli są).
3. **Wszczepienie implantów** — 4 lub 6, z nachyleniem implantów tylnych tam, gdzie trzeba ominąć zatokę/nerw.
4. **Protokół wspomagający gojenie** — **PRF** (fibryna z krwi pacjenta), **lasery Er:YAG / Nd:YAG** (biostymulacja i dezynfekcja) oraz **ozonoterapia**. Skracają rekonwalescencję i zmniejszają dolegliwości.
5. **Pomiar stabilności** — torque i ISQ (Osstell) potwierdzają, czy można obciążyć implanty natychmiast.
6. **Most tymczasowy w 24-48 h** — stałe zęby na czas gojenia.

O części chirurgicznej (ekstrakcje, PRF, zabiegi towarzyszące) piszemy też w dziale [chirurgia stomatologiczna](/oferta/chirurgia).

### Co dalej — gojenie i most ostateczny

Przez **3-6 miesięcy** implanty zrastają się z kością. W tym czasie:

* Nosisz most tymczasowy i normalnie funkcjonujesz (z rozsądkiem — unikasz nadmiernych, twardych obciążeń).
* Przychodzisz na kontrole; dbasz o higienę wokół konstrukcji.
* Po pełnej integracji wykonujemy **most ostateczny z pełnokonturowego cyrkonu, przykręcany** — trwały, estetyczny i możliwy do zdjęcia przez lekarza podczas okresowej kontroli.

### Najczęstsze obawy pacjentów

#### Czy stały most założony „od razu" jest pewny?

Tak — pod warunkiem, że implanty osiągnęły wymaganą stabilność pierwotną, co potwierdzamy pomiarem (torque ≥35 Ncm, ISQ). Most tymczasowy dodatkowo szynuje implanty, rozkładając obciążenie. Jeśli warunki nie są spełnione, nie obciążamy na siłę — odraczamy.

#### Czy mogę normalnie jeść pierwszego dnia?

Przez okres gojenia obowiązuje **dieta oszczędzająca** — miękkie pokarmy, unikanie twardych i bardzo wymagających kęsów. Most tymczasowy pozwala jeść i mówić komfortowo, ale to nie czas na orzechy i twarde skórki. Pełną funkcję odzyskujesz z mostem ostatecznym.

#### Czy będzie widać, że to „tymczasowe" zęby?

Most tymczasowy jest estetyczny i pełni funkcję na czas gojenia, ale most ostateczny z cyrkonu jest wykonany z najwyższą precyzją estetyczną. Najważniejsze: **od pierwszego dnia masz pełny łuk stałych zębów**, bez luki i bez protezy.

#### Zgrzytam zębami — czy to wyklucza zęby w jeden dzień?

Niekoniecznie wyklucza, ale wymaga ostrożności i zwykle **szyny ochronnej (bruxguard)**. Bruksizm bez zabezpieczenia to realne ryzyko dla świeżo wszczepionych implantów — dlatego oceniamy go indywidualnie i dobieramy postępowanie (czasem All-on-6 dla lepszego rozkładu sił).

#### Co jeśli u mnie nie da się obciążyć od razu?

Wtedy zastosujemy klasyczny protokół — most ostateczny po okresie gojenia. Efekt końcowy (stałe zęby na implantach) jest ten sam; różni się tylko moment założenia stałej pracy. Bezpieczeństwo implantów jest priorytetem.

