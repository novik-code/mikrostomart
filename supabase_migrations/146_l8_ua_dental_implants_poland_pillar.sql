### Поширені питання (FAQ)

#### Скільки часу триває повне лікування зубного імпланта?

Всього **4-7 місяців**, з яких 3-6 місяців припадає на період остеоінтеграції (інтеграції імпланта з кісткою). Зазвичай ви потребуєте 2-3 візити в Ополе — решту часу проводите вдома в Україні чи Польщі.

#### Чи лікар говорить українською?

Безпосереднє лікування проводиться польською, англійською або німецькою мовою. **WhatsApp-консультації і організація візиту — українською мовою через нашу рецепцію 24/7**. Для пацієнтів з України це означає повну зрозумілість на всіх етапах планування та післяопераційного догляду.

#### Чи є гарантія на імплант?

**Так.** Виробники імплантів (Straumann, Nobel Biocare, Astra Tech) надають довічну гарантію виробника на сам імплант. Mikrostomart гарантує правильну постановку імпланта протягом **5 років** і коронку протягом **2-3 років**. Деталі на сторінці [Гарантія](/ua/gwarancje).

#### Що робити, якщо після повернення додому виникнуть ускладнення?

Ви можете зв'язатися з нами **24/7 через WhatsApp українською мовою**. Для термінових випадків ми координуємо з місцевим стоматологом біля вашого місця проживання. У більшості випадків достатньо відеоконсультації та дистанційної оцінки. Повна інформація про ускладнення та наші протоколи лікування на сторінці [Ускладнення зубних імплантів](/ua/baza-wiedzy/uskladnennia-zubnyh-implantiv-profilaktyka-likuvannia).

#### Чи потрібен мені новий CBCT, якщо у мене вже є знімок з України?

**Так, ми рекомендуємо нове CBCT на місці.** Актуальна 3D-діагностика є необхідною для безпечного планування імплантації, і ми використовуємо найсучасніше обладнання. Повний діагностичний звіт надається в зрозумілій формі.

#### Які марки імплантів ви використовуєте?

Виключно **преміум-марки**: Straumann (Швейцарія), Nobel Biocare (США/Швеція), Astra Tech EV (Швеція/Велика Британія). Ми **не** використовуємо дешеві азіатські або генеричні марки — довгострокова стабільність та визнання міжнародними страховими компаніями для нас принципово важливі.

