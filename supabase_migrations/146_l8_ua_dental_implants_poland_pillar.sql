### Графік лікування — що відбувається в клініці?

Повне лікування зубного імпланта зазвичай триває **4-7 місяців**, причому українські пацієнти зазвичай потребують лише **2-3 візитів в Ополе**. Решта керується дистанційно через WhatsApp та телефон українською мовою.

#### Візит 1: Консультація + CBCT (День 1, 60 хв)

Анамнез, клінічне обстеження, 3D CBCT-діагностика, індивідуальний план лікування. Ви отримуєте **письмовий кошторис** у зрозумілому вигляді, придатний для подання у вашу страхову компанію (якщо у вас є приватне страхування) або для документації.

#### Візит 2: Постановка імпланта (День 2-7 після консультації, 60-90 хв на імплант)

Постановка імпланта під місцевою анестезією, з хірургічним мікроскопом ZEISS і застосуванням PRF для прискореного загоєння. Ви отримуєте **письмові інструкції догляду українською мовою**.

#### Період загоєння: Остеоінтеграція (3-6 місяців)

Повертаєтеся додому в Україну або до місця проживання у Польщі. Імплант інтегрується з щелепною кісткою. У цей час візити до клініки не потрібні — для будь-яких питань зв'яжіться з нами 24/7 через WhatsApp.

#### Візит 3: Абатмент + цифровий відбиток (День 90-180)

Другий візит в Ополе. Постановка абатмента і цифрове інтраоральне сканування для коронки.

#### Візит 4: Постановка коронки (День 100-200)

Третій і останній візит. Підгонка і фіксація індивідуально виготовленої коронки з повної кераміки або диоксиду цирконію.

