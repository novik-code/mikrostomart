### Поширені питання (FAQ)

#### Скільки часу триває повне лікування з імплантом?

Від першого дня до готової коронки зазвичай проходить **9-14 місяців**: кісткова аугментація (1 день) + загоєння (4-9 місяців) + постановка імпланта (1 день) + остеоінтеграція (3-6 місяців) + підгонка коронки (1-2 дні). Для негайної аугментації з негайною імплантацією процес скорочується до **6-9 місяців**.

#### Чи болить процедура кісткового трансплантата?

Під час процедури **ні** — ми працюємо під місцевою анестезією (опціонально з седацією закисом азоту для тривожних пацієнтів). У перші 2-3 дні після процедури дискомфорт легкий до помірного і добре контролюється стандартними знеболюючими (ібупрофен 400 мг + парацетамол 500 мг). Ви отримуєте детальні письмові інструкції з післяопераційного догляду українською мовою.

#### Чи можу я уникнути кісткової аугментації — чи є альтернативи?

У деяких випадках, так. Альтернативи включають:

* **Короткі імпланти** (4-6 мм) — для пацієнтів з помірною вертикальною резорбцією кістки
* **Кутове розміщення** (напр. концепція All-on-4) — імпланти встановлюються в щільну передню щелепну кістку без трансплантації
* **Міст** на сусідніх зубах — якщо ви хочете уникнути імплантів
* **Знімний протез** — найекономічніше рішення, але функціонально та естетично нижчого рівня

Який варіант найкращий для вас, ми з'ясуємо під час первинної консультації з CBCT.

#### Чи безпечний Bio-Oss®? Він же зрештою походить з коров'ячої кістки.

**Так, Bio-Oss® є високобезпечним.** Матеріал обробляється за патентованою процедурою при високих температурах до повністю **демінералізованої форми без білка** — усі органічні компоненти (білки, ДНК, клітини) усуваються. Залишається чиста неорганічна кісткова матриця, яка служить як остеокондуктивна каркасна основа. Використовується по всьому світу з 1980-х років, з понад 1 000 науковими публікаціями, що підтверджують безпеку та довгостроковий прогноз. Для пацієнтів з етичними сумнівами щодо ксеногенних матеріалів ми пропонуємо **синтетичні альтернативи** (Cerasorb®).

#### Що відбувається, якщо кістковий трансплантат не вдасться?

