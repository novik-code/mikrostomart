### Чотири основні методи кісткової аугментації

Вибір методу залежить від обсягу та локалізації кісткового дефіциту. Ми регулярно поєднуємо кілька технік в одну сесію.

#### 1. Guided Bone Regeneration (GBR) — Спрямована регенерація кістки

GBR — це **найбільш поширений метод** для помірних горизонтальних або вертикальних дефектів. Кістковий замінник (зазвичай Bio-Oss®) накладається на оголену кістку та покривається **резорбованою колагеновою мембраною** (Bio-Gide®). Мембрана перешкоджає вростанню м'яких тканин і дає кісткоутворюючим клітинам час (4-9 місяців) перебудувати трансплантат у стабільну, життєздатну кістку.

**Типові показання:** локальні горизонтальні дефекти після екстракцій, периімплантні дефекти, реконструкція дефектів перед імплантацією.

#### 2. Синусліфт (внутрішній і зовнішній)

При **синусліфт-процедурі** дно верхньощелепної пазухи піднімається, щоб створити простір для імпланта в задньому верхньому щелепі. Два варіанти:

* **Внутрішній синусліфт** — малоінвазивна техніка через заплановане місце імплантата, підходить для помірної резорбції кістки (висота залишкової кістки >5 мм). Час загоєння: 3-4 місяці, часто можлива негайна імплантація.
* **Зовнішній синусліфт (латеральне вікно)** — для сильно резорбованої верхньощелепної пазухи (<5 мм). Трансплантат вводиться через підготовлене кісткове вікно. Час загоєння: 6-9 місяців перед імплантацією.

Показники успіху зовнішнього синусліфта: **95-98%** через 10 років (Wallace & Froum 2003, *Annals of Periodontology*).

#### 3. Аутологічна кісткова трансплантація

Власна кістка пацієнта з **кута щелепи** або **підборіддя** вважається **біологічним золотим стандартом** — відсутність реакції на чужорідний матеріал, найшвидша інтеграція. Ми використовуємо аутологічну кістку переважно для **великих вертикальних дефектів** та у комбінації з Bio-Oss® (так званий «композитний трансплантат»). Забір з гребеня клубової кістки (для багатьох імплантів одночасно) виконується вкрай рідко і зазвичай у співпраці з щелепно-лицевим хірургом у лікарняних умовах.

#### 4. Негайна імплантація з PRF в екстракційній альвеолі

Коли зуб видаляється і необхідно негайно встановити імплант, ми заповнюємо проміжок між імплантатом та альвеолярною стінкою **PRF з власної крові** і частинками Bio-Oss®. Ця техніка уникає окремої процедури аугментації і скорочує загальний час лікування на 4-6 місяців. Це не завжди можливо — придатність оцінюється через CBCT-знімок під час первинної консультації.

