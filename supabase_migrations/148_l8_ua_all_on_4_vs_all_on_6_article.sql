### All-on-4 — оригінальна концепція

**Концепція All-on-4** була розроблена на початку 2000-х років д-ром Пауло Мало (Лісабон) і опублікована в новаторському дослідженні в 2003 році. Ідея: **чотири стратегічно розташовані імпланти** достатні для надійного закріплення повного фіксованого мосту (12-14 зубів).

**Характерні особливості:**

* **2 передні імпланти** — розміщені прямо, у щільній кістці нижче різців
* **2 дистальні імпланти** — розміщені **під кутом 30-45° назад**, щоб обійти верхньощелепну пазуху (верхня щелепа) або нервовий канал (нижня щелепа)
* **Негайне навантаження** з тимчасовим мостом у день операції
* **Фаза консолідації** 3-4 місяці, потім фінальний міст

Ключова перевага: **кутове розміщення обходить анатомічні перешкоди** — для більшості пацієнтів [кісткова аугментація](/ua/baza-wiedzy/kistkova-augmentatsiia-v-polshchi-metody-tsiny) **не потрібна**. Це драматично скорочує загальний час лікування (з 9-14 місяців до 4-6 місяців) і знижує витрати.

### All-on-6 — більш консервативний варіант

**Концепція All-on-6** використовує **шість імплантів** замість чотирьох. Вона була розроблена, серед інших, д-ром Карлом Мішем (США) як альтернативна стратегія для складних випадків.

**Характерні особливості:**

* **6 імплантів розподілених** по всій щелепній дузі
* **Переважно пряме розміщення** (без екстремальної кутової постановки)
* **Більше імплантів = краще розподіл навантаження** — кожен імплант несе менше механічне навантаження
* **Розширене показання** — також для пацієнтів з бруксизмом, слабкою якістю кістки або попередніми втратами імплантів
* **Кісткова аугментація частіше необхідна** — для сильно резорбованих щелеп часто потрібен синусліфт або GBR

### Основні відмінності — огляд

**Кількість імплантів**

* **All-on-4:** 4 імпланти (2 передні прямі + 2 дистальні кутові)
* **All-on-6:** 6 імплантів (розподілені, переважно прямі)

**Кутове vs пряме розміщення**

* **All-on-4:** дистальні імпланти сильно кутові (30-45°) — обходять анатомічні перешкоди
* **All-on-6:** переважно пряме розміщення — біомеханічно більш консервативне

**Розподіл навантаження на імплант**

* **All-on-4:** кожен імплант несе приблизно 25% жувального навантаження
* **All-on-6:** кожен імплант несе приблизно 17% — менше індивідуальне навантаження, краще при бруксизмі

**Необхідність кісткової аугментації**

* **All-on-4:** зазвичай **не потрібна** (кутове розміщення обходить синусліфт/GBR)
* **All-on-6:** **часто потрібна** для резорбованих щелеп — продовжує час лікування

**Тривалість лікування**

* **All-on-4:** 4-6 місяців (без трансплантації), часто негайне навантаження
* **All-on-6:** 6-9 місяців без трансплантації, 9-14 місяців з трансплантацією

**Витрати**

* **All-on-4 з негайним навантаженням:** 30 000-40 000 PLN (~225 000-300 000 UAH / €7 000-9 300)
* **All-on-6 з негайним навантаженням:** 45 000-55 000 PLN (~340 000-415 000 UAH / €10 500-12 800)
* **Різниця у вартості:** приблизно 15 000 PLN (~115 000 UAH)

**Показники успіху (10-річні дослідження)**

* **All-on-4 (Maló 2011, 2019):** 96-98% виживання імплантів, 99,2% виживання протезів
* **All-on-6 (протоколи Misch):** 97-99% — мінімально більше безпеки на імплант при вищій кількості імплантів, але статистично сходиться

**Схильність до догляду та ремонту**

* **All-on-4:** менше імплантів = менше потенційних місць ускладнень
* **All-on-6:** при втраті одного імпланта міст часто може продовжувати функціонувати (більше резерву)

### Коли All-on-4 — правильний вибір

**Концепція All-on-4** є правильним варіантом, якщо до вас застосовуються наступні фактори:

* **Сильно резорбована щелепна кістка** — ви хочете уникнути кісткової аугментації
* **Обмежений бюджет** — All-on-4 приблизно на 30% дешевший за All-on-6
* **Бажане швидке лікування** — ви хочете якнайшвидше повернутися до нормального жування і мови
* **Без сильного бруксизму** — ви не натискаєте і не скрегочете зубами сильно
* **Хороше системне здоров'я** — без неконтрольованого діабету, без сильного куріння
* **Перевага простого догляду за ротовою порожниною** — 4 імпланти легше чистити, ніж 6

Більшість наших **пацієнтів з України** з повною беззубістю верхньої щелепи отримують користь від концепції All-on-4 — коротший час лікування (часто 2 візити замість 4) означає менше подорожей, менше ночівлі в готелі, менше використаних робочих днів.

### Коли All-on-6 — правильний вибір

**Концепція All-on-6** є правильним варіантом у наступних ситуаціях:

* **Достатній об'єм кістки** (або кісткова аугментація не є перешкодою)
* **Сильний бруксизм або скрегіт** — більше імплантів розподіляють надмірні сили
* **Попередні втрати імплантів** — ви хочете мати резерв безпеки
* **Доступний вищий бюджет** — додаткова вартість 15 000 PLN (~115 000 UAH) для преміум-варіанту
* **Пріоритет тривалішого терміну служби зубного протеза** — для пацієнтів молодше 60 років зі здоровим способом життя
* **Важлива задня стабільність** — для пацієнтів, які сильно жують задніми зубами

**All-on-6 є "більш консервативним" вибором у позитивному сенсі** — більше резервів для довшого терміну служби реставрації.

### Гібрид: All-on-4 плюс 2 (компромісний варіант)

Менш відомим, але клінічно цінним варіантом є **All-on-4 плюс 2**: ви починаєте з класичної концепції All-on-4 і доповнюєте її **двома додатковими передніми імплантами**. Результат: 6 розподілених імплантів, але з логікою негайного навантаження підходу All-on-4.

**Показання:**

* **Пацієнти між All-on-4 і All-on-6** — хочуть резерв безпеки, але без зусиль повної кісткової аугментації
* **Можливе поетапне лікування** — спочатку All-on-4 з негайним навантаженням, пізніше додаткові 2 імпланти
* **Витрати між двома концепціями:** приблизно 37 000-47 000 PLN (~280 000-355 000 UAH / €8 600-11 000)

### Наукові дані — довгострокові дослідження

Порівняльні дані для обох концепцій детально задокументовані:

* **Maló et al. 2003** — оригінальне дослідження All-on-4 на 44 пацієнтах, 100% виживання імплантів через 1 рік
* **Maló et al. 2011** — 5-річне дослідження на 245 пацієнтах: 97% виживання імплантів, 99% виживання протезів
* **Maló et al. 2019** — **18-річне спостереження** оригінальних пацієнтів: 94,8% виживання імплантів, 99,2% виживання протезів
* **Soto-Penaloza et al. 2017** — систематичний огляд All-on-4: 99,2% виживання протезів у всіх дослідженнях
* **Eliasson et al. 2010** — Концепції з шістьма імплантами: 98,7% успіх протягом 5 років
* **ITI Treatment Guide 2019** — обидві концепції визнані доказово обґрунтованими

**Ключовий урок:** При правильному показанні обидві концепції є **однаково успішними**. Рішення не повинно базуватися переважно на принципі "більше = краще", а на індивідуальних клінічних факторах.

