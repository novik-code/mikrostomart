### Поширені питання (FAQ)

#### Яка концепція більш довговічна — All-on-4 чи All-on-6?

При правильному показанні обидві концепції є **порівнянно довговічними**. 18-річне спостереження Maló (2019) показує для All-on-4 показник успіху 94,8% виживання імплантів — значення, які для All-on-6 мінімально вищі, але статистично практично сходяться. **Більш важливі ніж кількість імплантів** є фактори як якість імпланта (преміум-марки!), правильне показання та комплаєнс гігієни ротової порожнини.

#### Чи фіксований міст буде виглядати як натуральна щелепа?

**Так.** Для обох — All-on-4 і All-on-6 — ми створюємо індивідуальні мости з повної кераміки або диоксиду цирконію (12-14 зубів на щелепу). З цифровим макетом ви бачите запланований результат **перед** операцією і можете запросити коригування. Натуральний вигляд не відрізняється.

#### Чи можу я пізніше розширити з All-on-4 до All-on-6?

**Так, це логіка концепції "All-on-4 плюс 2".** Ми спочатку встановлюємо All-on-4, і при необхідності (напр. після втрати імпланта або додаткового навантаження) доповнюємо двома додатковими передніми імплантами. Це повинно бути сплановано з самого початку.

#### Що відбувається, якщо один імплант виходить з ладу?

При **All-on-6** міст зазвичай може продовжувати функціонувати на залишених 5 імплантах — у вас є резерв. При **All-on-4** збій імпланта більш критичний (залишається тільки 3 з 4); зазвичай вимагає реімплантації. Детальна інформація про управління ускладненнями на сторінці [Ускладнення зубних імплантів](/ua/baza-wiedzy/uskladnennia-zubnyh-implantiv-profilaktyka-likuvannia).

#### Чи мені дійсно не потрібна кісткова аугментація для All-on-4?

**У більшості випадків ні.** Кутове розміщення дистальних імплантів (30-45°) дозволяє встановити імпланти в щільну передню кістку та обійти верхньощелепну пазуху. Для надзвичайно сильно резорбованих щелеп [кісткова аугментація](/ua/baza-wiedzy/kistkova-augmentatsiia-v-polshchi-metody-tsiny) може все ще бути необхідною — остаточне рішення приймається після оцінки CBCT.

