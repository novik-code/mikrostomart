export async function generateMetadata(): Promise<Metadata> {
    // Load brand from DB (falls back to hardcoded defaults on error)
    const b = await loadBrandFromDB();
    // J-4: dynamic og:locale per request — slug/listing pages without their
    // own pageMetadata() previously inherited the hardcoded pl_PL even on
    // /en/* and /de/* URLs. getLocale() reads the next-intl middleware's
    // resolved locale so the inherited tag matches the request.
    const locale = await getLocale();
    return {
        metadataBase: new URL(b.metadataBase),
        alternates: {
            canonical: './',
            // Per-page hreflang declared in each [locale]/<path>/layout.tsx via pageMetadata()
            // helper from @/lib/seo. Each public page now emits correct per-URL alternates
            // (e.g. /oferta → en:/en/oferta, de:/de/oferta, uk:/ua/oferta).
            // Root layout intentionally omits a global languages map — falsely declaring
            // every subpage's foreign-locale equivalent as the homepage was confusing Google.
            // Internal pages without per-page metadata (admin, pracownik) are disallowed in
            // robots.txt anyway, so they don't need hreflang.
        },
        title: {
            default: b.titleDefault,
            template: b.titleTemplate,
        },
        description: b.description,
        keywords: b.keywords,
        manifest: "/manifest.json",
        icons: {
            icon: '/icon.png',
            shortcut: '/icon.png',
            apple: '/icon-512x512.png',
        },
        openGraph: {
            type: 'website',
            locale: getOgLocale(locale),
            siteName: b.ogSiteName,
            // J-3: homepage-specific 1200×630 OG card (generated by
            // scripts/generate-og-images.mjs). Service pages override this
            // per-page via pageMetadata({ ogImage }); other inheriting pages
            // (FAQ, cennik, kontakt, etc.) fall back to og-home.
            images: [{
                url: '/og-home.webp',
                width: 1200,
                height: 630,
                alt: b.ogImageAlt,
            }],
        },
        twitter: {
            card: 'summary_large_image',
            images: ['/og-home.webp'],
        },
        other: isDemoMode ? undefined : {
            "geo.region": b.geoRegion,
            "geo.placename": b.geoPlacename,
            "geo.position": b.geoPosition,
            "ICBM": b.icbm,
            // J-5 follow-up #3 (2026-05-12): Business Suite domain claim. Uses
            // `name=` (different convention from fb:app_id below) which is what
            // Facebook's domain verification scanner explicitly looks for.
            ...(b.facebookDomainVerification ? { "facebook-domain-verification": b.facebookDomainVerification } : {}),
        },
        // NOTE on fb:app_id: Next.js `other` always emits `<meta name="…">`,
        // but Facebook's scraper only honours `<meta property="fb:app_id">`.
        // We render it manually in the <head> below — see RootLayout.
        appleWebApp: {
            capable: true,
            title: 'Mikrostomart',
            statusBarStyle: 'black-translucent',
        },
        applicationName: 'Mikrostomart',
    };
}

